package com.India;

public class School {
	 String Name;
	 int Age;
	


	public School(String Name, int Age) {
		
		
		this.Name = Name;
		this.Age = Age;
		
		
	}



	@Override
	public String toString() {
		return "School [Name=" + Name + ", Age=" + Age + "]";
	}
	

	

	

	
	

}
